# Script verificar se os numero são iguais
def main():
    num1 = int(input('Digite um Número: '))
    num2 = int(input('Digite mais um Número: '))

    if num1 == num2:
        print(f'Os Números {num1} e {num2} são iguais!')

    else:
        print(f'Os Números {num1} e {num2} não são iguais!')

main()
